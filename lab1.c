#include <stdio.h>
#include <stdlib.h>
#include <pthread.h>
#include <unistd.h>
#include <sys/sysinfo.h>  /* get_nprocs_conf(), get_nprocs() */

/* Структура для передачи параметров в поток */
typedef struct {
    int  flag;  /* флаг завершения: 0 — работать, 1 — завершиться */
    char sym;   /* символ, который выводит поток */
} targs;

/* ------------------------------------------------------------------ */
/* Поточная функция 1                                                   */
/* ------------------------------------------------------------------ */
static void *thread_func1(void *arg)
{
    printf("поток 1 начал работу\n");
    fflush(stdout);

    targs *args = (targs *)arg;  /* приводим обобщённый указатель void*
                                    к конкретному типу targs* */

    while (args->flag == 0) {   /* цикл работает, пока флаг не установлен */
        putchar(args->sym);     /* вывод символа потока на экран */
        fflush(stdout);         /* сброс буфера вывода — символ появляется
                                   сразу, без накопления в буфере */
        sleep(1);               /* задержка 1 с для наглядности чередования
                                   потоков */
    }

    printf("\nпоток 1 закончил работу\n");
    fflush(stdout);

    /* pthread_exit — завершение текущего потока;
       параметр (void*)1 — код завершения, который можно прочитать
       в main() через pthread_join */
    pthread_exit((void *)1);
    return NULL;
}

/* ------------------------------------------------------------------ */
/* Поточная функция 2                                                   */
/* ------------------------------------------------------------------ */
static void *thread_func2(void *arg)
{
    printf("поток 2 начал работу\n");
    fflush(stdout);

    targs *args = (targs *)arg;  /* приводим обобщённый указатель void*
                                    к конкретному типу targs* */

    while (args->flag == 0) {   /* цикл работает, пока флаг не установлен */
        putchar(args->sym);     /* вывод символа потока на экран */
        fflush(stdout);         /* сброс буфера вывода */
        sleep(1);               /* задержка 1 с для наглядности */
    }

    printf("\nпоток 2 закончил работу\n");
    fflush(stdout);

    /* pthread_exit — завершение текущего потока с кодом 2 */
    pthread_exit((void *)2);
    return NULL;
}

/* ------------------------------------------------------------------ */
/* Основная программа                                                   */
/* ------------------------------------------------------------------ */
int main(void)
{
    printf("программа начала работу\n");

    /* --- Дополнительное задание: информация о процессорах ---------- */

    /* get_nprocs_conf — возвращает число процессоров, сконфигурированных
       в системе (т.е. известных ядру ОС согласно описанию оборудования).
       Объявлена в <sys/sysinfo.h>.
       Сконфигурированный процессор — это процессор, о котором знает ядро;
       он может быть как включён, так и временно отключён (offline). */
    int nconf = get_nprocs_conf();
    printf("get_nprocs_conf()  = %d  (сконфигурировано процессоров)\n", nconf);

    /* get_nprocs — возвращает число процессоров, доступных в данный момент
       (online, т.е. реально используемых планировщиком ОС).
       Объявлена в <sys/sysinfo.h>.
       Это число всегда <= get_nprocs_conf(), потому что часть процессоров
       может быть отключена оператором или ядром (например, для экономии
       энергии или в виртуальной машине с ограниченными ресурсами). */
    int navail = get_nprocs();
    printf("get_nprocs()       = %d  (доступно процессоров)\n", navail);

    /* Почему числа могут различаться:
       - В виртуальных машинах гипервизор может предоставлять системе
         одно число vCPU, но разрешать использовать меньше.
       - Администратор может вручную перевести процессор в offline:
           echo 0 > /sys/devices/system/cpu/cpu1/online
       - Ядро само отключает неисправные или перегретые ядра.
       - При горячем добавлении CPU (CPU hotplug) новый процессор сначала
         становится сконфигурированным, но ещё не доступным. */

    /* sysconf — универсальная POSIX-функция получения системных параметров
       во время выполнения программы; объявлена в <unistd.h>.
       _SC_NPROCESSORS_CONF — константа-запрос: число сконфигурированных
                              процессоров (аналог get_nprocs_conf).
       _SC_NPROCESSORS_ONLN — константа-запрос: число доступных (online)
                              процессоров (аналог get_nprocs).
       При ошибке sysconf возвращает -1. */
    long sc_conf = sysconf(_SC_NPROCESSORS_CONF);
    long sc_onln = sysconf(_SC_NPROCESSORS_ONLN);
    printf("sysconf(_SC_NPROCESSORS_CONF) = %ld  (сконфигурировано, через sysconf)\n", sc_conf);
    printf("sysconf(_SC_NPROCESSORS_ONLN) = %ld  (доступно, через sysconf)\n", sc_onln);

    /* --- Конец дополнительного задания ----------------------------- */

    pthread_t id1, id2;  /* идентификаторы (дескрипторы) потоков */

    /* Флаги завершения объявлены локально в main() и передаются
       в потоки по адресу, как требует задание */
    targs arg1 = {0, '1'};
    targs arg2 = {0, '2'};

    /* pthread_create — создание потока;
       &id1          — указатель на дескриптор создаваемого потока;
       NULL          — атрибуты потока (используются значения по умолчанию);
       thread_func1  — поточная функция, которая станет телом потока;
       &arg1         — указатель на передаваемые в поток параметры */
    int rv1 = pthread_create(&id1, NULL, thread_func1, &arg1);
    if (rv1 != 0) {  /* при ошибке pthread_create возвращает ненулевой код */
        perror("pthread_create 1");
        return 1;
    }

    /* pthread_create — создание второго потока (аналогично первому) */
    int rv2 = pthread_create(&id2, NULL, thread_func2, &arg2);
    if (rv2 != 0) {
        perror("pthread_create 2");
        return 1;
    }

    printf("программа ждет нажатия клавиши\n");

    /* getchar — блокирующее ожидание нажатия клавиши <Enter>;
       пока функция не вернёт управление, потоки продолжают работать */
    getchar();

    printf("клавиша нажата\n");

    /* Устанавливаем флаги завершения — потоки увидят изменение
       на следующей итерации цикла и корректно выйдут */
    arg1.flag = 1;
    arg2.flag = 1;

    void *exitcode1 = NULL;
    void *exitcode2 = NULL;

    /* pthread_join — ожидание завершения потока с идентификатором id1;
       &exitcode1   — адрес, куда будет записан код завершения потока,
                      переданный через pthread_exit */
    pthread_join(id1, &exitcode1);

    /* pthread_join — ожидание завершения потока с идентификатором id2 */
    pthread_join(id2, &exitcode2);

    printf("поток 1 завершился с кодом: %ld\n", (long)exitcode1);
    printf("поток 2 завершился с кодом: %ld\n", (long)exitcode2);

    printf("программа завершила работу\n");
    return 0;
}
