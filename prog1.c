/* Программа 1 — дочерняя программа.
   Запускается родительской программой (prog2) через fork() + execlp().
   Принимает аргументы командной строки, выводит их на экран,
   а также выводит свой pid и ppid. Завершается с кодом 42. */

#include <stdio.h>
#include <stdlib.h>
#include <unistd.h>   /* getpid(), getppid(), sleep() */

int main(int argc, char *argv[])
{
    /* getpid — получение идентификатора текущего (дочернего) процесса */
    /* getppid — получение идентификатора родительского процесса */
    printf("prog1: запущен. pid=%d  ppid=%d\n", getpid(), getppid());

    /* Выводим аргументы командной строки, переданные через execlp() */
    printf("prog1: получено аргументов: %d\n", argc - 1);
    for (int i = 1; i < argc; i++) {
        /* sleep — задержка 1 с между выводами аргументов */
        sleep(1);
        printf("prog1: аргумент[%d] = '%s'  pid=%d  ppid=%d\n",
               i, argv[i], getpid(), getppid());
    }

    /* exit — завершение процесса с кодом 42;
       код завершения будет прочитан родителем через waitpid() */
    printf("prog1: завершаю работу с кодом 42\n");
    exit(42);
}
