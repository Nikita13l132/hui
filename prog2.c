/* Программа 2 — родительская программа.
   Принимает 3–5 аргументов командной строки, выводит их на экран,
   затем создаёт дочерний процесс через fork() и запускает в нём
   prog1 с помощью execlp().

   execlp — вариант функции exec с суффиксами:
     'l' — аргументы передаются списком (arg0, arg1, ..., NULL),
           поэтому передаём часть аргументов (2–3 штуки);
     'p' — программа ищется по каталогам переменной среды PATH,
           поэтому передаём только имя "prog1" без "./".
   Чтобы execlp нашла prog1 по PATH, добавляем текущий каталог в PATH.

   Родитель ожидает завершения дочернего процесса неблокирующим
   waitpid(WNOHANG), проверяя каждые 0.5 секунды. */

#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include <unistd.h>    /* fork(), getpid(), getppid(), sleep() */
#include <sys/wait.h>  /* waitpid(), WNOHANG, WEXITSTATUS() */

int main(int argc, char *argv[])
{
    if (argc < 4 || argc > 6) {
        fprintf(stderr, "Использование: %s арг1 арг2 арг3 [арг4 [арг5]]\n",
                argv[0]);
        return 1;
    }

    /* getpid  — идентификатор текущего (родительского) процесса */
    /* getppid — идентификатор процесса-деда (родителя prog2) */
    printf("prog2: запущен. pid=%d  ppid=%d\n", getpid(), getppid());

    /* Выводим аргументы командной строки prog2 */
    printf("prog2: получено аргументов: %d\n", argc - 1);
    for (int i = 1; i < argc; i++) {
        printf("prog2: аргумент[%d] = '%s'\n", i, argv[i]);
    }

    /* Добавляем текущий каталог в PATH, чтобы execlp нашла prog1
       без явного указания пути "./prog1".
       Суффикс 'p' в execlp означает поиск по PATH. */
    char *old_path = getenv("PATH"); /* getenv — чтение переменной среды PATH */
    char new_path[1024];
    snprintf(new_path, sizeof(new_path), ".:%s", old_path ? old_path : "");
    /* setenv — установка переменной среды PATH для текущего процесса
       и всех его потомков; 1 — перезаписать существующее значение */
    setenv("PATH", new_path, 1);
    printf("prog2: PATH дополнен текущим каталогом\n");

    /* fork — создание дочернего процесса;
       возвращает 0 в дочернем процессе,
       pid дочернего — в родительском,
       -1 — при ошибке */
    pid_t pid = fork();

    if (pid == -1) {
        perror("fork");
        return 1;

    } else if (pid == 0) {
        /* --- Дочерний процесс ---------------------------------------- */

        /* getpid  — идентификатор дочернего процесса */
        /* getppid — идентификатор родительского процесса (prog2) */
        printf("prog2(дочерний): pid=%d  ppid=%d — запускаю prog1 через execlp\n",
               getpid(), getppid());

        /* execlp — запуск внешней программы вместо текущего процесса;
           arg0 "prog1" — имя файла для поиска по PATH (суффикс 'p');
           arg0 "prog1" — argv[0] запускаемой программы (по соглашению имя);
           arg1..arg3   — 3 аргумента из командной строки prog2 (суффикс 'l':
                          аргументы передаются списком, последний — NULL);
           при успехе не возвращается; при ошибке возвращает -1 */
        execlp("prog1", "prog1", argv[1], argv[2], argv[3], NULL);

        /* Если execlp вернулась — произошла ошибка */
        perror("execlp");
        exit(1);

    } else {
        /* --- Родительский процесс ------------------------------------ */

        printf("prog2(родитель): дочерний процесс создан, pid дочернего=%d\n",
               pid);

        /* Ожидаем завершения дочернего процесса без блокировки.
           waitpid — ожидание завершения процесса с идентификатором pid;
           &status  — переменная для получения кода завершения;
           WNOHANG  — неблокирующий режим: если дочерний ещё не завершился,
                      функция сразу возвращает 0, не приостанавливая родителя;
           возвращает pid завершившегося процесса, 0 (ещё не завершился)
           или -1 (ошибка) */
        int status;
        pid_t rv;
        while (1) {
            rv = waitpid(pid, &status, WNOHANG);
            if (rv == -1) {
                perror("waitpid");
                return 1;
            } else if (rv == 0) {
                /* дочерний процесс ещё работает */
                printf("prog2(родитель): ждём завершения дочернего...\n");
                /* usleep — задержка в микросекундах;
                   500000 мкс = 0.5 сек, как требует задание */
                usleep(500000);
            } else {
                /* rv == pid: дочерний процесс завершился */
                break;
            }
        }

        /* WIFEXITED — макрос: проверяет, завершился ли процесс штатно
           через exit() или return из main();
           WEXITSTATUS — макрос: извлекает код завершения из status */
        if (WIFEXITED(status)) {
            printf("prog2(родитель): дочерний завершился с кодом %d\n",
                   WEXITSTATUS(status));
        } else {
            printf("prog2(родитель): дочерний завершился нештатно\n");
        }
    }

    printf("prog2: завершаю работу\n");
    return 0;
}
