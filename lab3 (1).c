#define _GNU_SOURCE          /* открывает pipe2() и другие GNU-расширения */
/* Лабораторная работа 3 — Взаимодействие потоков через неименованный канал.
   Поток-писатель получает домашний каталог текущего пользователя
   функцией getpwnam_r() (функция №17 из таблицы) и записывает
   результат в неименованный канал.
   Поток-читатель читает данные из канала и выводит их на экран.

   Режим работы канала задаётся аргументом командной строки (-m):
     ./lab3 -m 1   pipe()  — блокирующий режим
     ./lab3 -m 2   pipe2() — неблокирующий режим (Linux)
     ./lab3 -m 3   pipe() + fcntl() — неблокирующий режим (POSIX)  */

#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include <unistd.h>
#include <fcntl.h>       /* fcntl(), O_NONBLOCK */
#include <signal.h>      /* signal(), SIGINT */
#include <errno.h>
#include <pwd.h>         /* getpwnam_r(), struct passwd */
#include <pthread.h>

/* ------------------------------------------------------------------ */
/* Глобальные переменные                                               */
/* ------------------------------------------------------------------ */

/* Дескрипторы неименованного канала:
   pipefd[0] — для чтения, pipefd[1] — для записи */
static int pipefd[2];

/* ------------------------------------------------------------------ */
/* Обработчик сигнала SIGINT (Ctrl+C)                                  */
/* ------------------------------------------------------------------ */

/* sig_handler — заменяет системный обработчик SIGINT;
   вызывается при нажатии Ctrl+C, если поток заблокирован в read/write;
   закрывает дескрипторы канала, чтобы разблокировать потоки, и завершает
   программу через exit() — при этом освобождаются ресурсы ОС */
static void sig_handler(int signo)
{
    printf("\nполучен сигнал SIGINT (%d), завершаем программу\n", signo);
    fflush(stdout);
    /* close — закрытие дескрипторов канала;
       закрытие дескриптора записи разблокирует заблокированный read()
       (он вернёт 0 — «конец файла»);
       закрытие дескриптора чтения разблокирует заблокированный write()
       (он вернёт -1 с errno = EPIPE) */
    close(pipefd[1]);
    close(pipefd[0]);
    exit(0);
}

/* ------------------------------------------------------------------ */
/* Структура параметров потоков                                        */
/* ------------------------------------------------------------------ */
typedef struct {
    int flag;  /* флаг завершения: 0 — работать, 1 — завершиться */
} targs;

/* ------------------------------------------------------------------ */
/* Поток-писатель: получает pw_dir через getpwnam_r() и пишет в канал */
/* ------------------------------------------------------------------ */
static void *thread_writer(void *arg)
{
    printf("поток писатель начал работу\n");
    fflush(stdout);

    targs *args = (targs *)arg; /* приводим void* к типу targs* */

    /* Буфер для getpwnam_r и буфер сообщения */
    char pw_buf[1024];
    char msg[256];

    while (args->flag == 0) {

        struct passwd pwd;
        struct passwd *result = NULL;

        /* getpwuid(getuid()) — получение имени пользователя по числовому uid;
           getuid()  — возвращает реальный uid текущего процесса;
           используется вместо getlogin(), так как getlogin() требует
           управляющего терминала и может вернуть NULL в нетерминальных
           сессиях (скрипты, демоны и т.п.) */
        struct passwd *pw_tmp = getpwuid(getuid());
        if (pw_tmp == NULL) {
            perror("getpwuid");
            sleep(1);
            continue;
        }
        char *username = pw_tmp->pw_name;

        /* getpwnam_r — потокобезопасная функция получения записи из
           базы данных паролей /etc/passwd по имени пользователя;
           name    — имя пользователя для поиска;
           &pwd    — структура, куда будет записан результат;
           pw_buf  — вспомогательный буфер для строковых полей структуры;
           sizeof  — размер вспомогательного буфера;
           &result — указатель на результат: если запись найдена,
                     *result == &pwd; если не найдена — *result == NULL;
           возвращает 0 при успехе, код ошибки при неудаче */
        int rv = getpwnam_r(username, &pwd, pw_buf, sizeof(pw_buf), &result);
        if (rv != 0) {
            /* ненулевой код — ошибка выполнения функции */
            fprintf(stderr, "getpwnam_r ошибка: %s\n", strerror(rv));
            sleep(1);
            continue;
        }
        if (result == NULL) {
            /* result == NULL — пользователь не найден в базе */
            fprintf(stderr, "getpwnam_r: пользователь '%s' не найден\n", username);
            sleep(1);
            continue;
        }

        /* Формируем сообщение из поля pw_dir (домашний каталог пользователя) */
        snprintf(msg, sizeof(msg), "pw_dir='%s'", pwd.pw_dir);

        /* write — запись сообщения в канал;
           pipefd[1] — дескриптор для записи;
           msg       — буфер с данными;
           strlen+1  — передаём вместе с завершающим нулём;
           возвращает число записанных байт или -1 при ошибке */
        ssize_t wr = write(pipefd[1], msg, strlen(msg) + 1);
        if (wr > 0) {
            printf("[писатель] записано %zd байт: %s\n", wr, msg);
            fflush(stdout);
        } else if (wr == -1) {
            if (errno == EAGAIN) {
                /* EAGAIN в неблокирующем режиме: канал полон */
                printf("[писатель] канал полон (EAGAIN), повтор через 1 сек\n");
                fflush(stdout);
            } else {
                perror("[писатель] write ошибка");
            }
        }

        sleep(1); /* задержка 1 с между отправками */
    }

    printf("поток писатель закончил работу\n");
    fflush(stdout);
    pthread_exit((void *)1);
    return NULL;
}

/* ------------------------------------------------------------------ */
/* Поток-читатель: читает из канала и выводит на экран                 */
/* ------------------------------------------------------------------ */
static void *thread_reader(void *arg)
{
    printf("поток читатель начал работу\n");
    fflush(stdout);

    targs *args = (targs *)arg; /* приводим void* к типу targs* */

    char buf[256];

    while (args->flag == 0) {

        /* Очищаем буфер перед каждым чтением */
        memset(buf, 0, sizeof(buf));

        /* read — чтение данных из канала;
           pipefd[0] — дескриптор для чтения;
           buf       — буфер для принятых данных;
           sizeof    — максимальное число байт для чтения;
           в блокирующем режиме: если канал пуст — поток блокируется;
           в неблокирующем режиме: если канал пуст — возвращает -1, errno=EAGAIN;
           возвращает число прочитанных байт, 0 (конец файла) или -1 (ошибка) */
        ssize_t rd = read(pipefd[0], buf, sizeof(buf) - 1);

        if (rd > 0) {
            /* данные успешно прочитаны */
            printf("[читатель] прочитано %zd байт: %s\n", rd, buf);
            fflush(stdout);
        } else if (rd == -1) {
            if (errno == EAGAIN) {
                /* EAGAIN в неблокирующем режиме: канал пуст */
                printf("[читатель] канал пуст (EAGAIN), повтор через 1 сек\n");
                fflush(stdout);
                sleep(1); /* пауза перед следующей попыткой */
            } else {
                perror("[читатель] read ошибка");
            }
        } else {
            /* rd == 0: все дескрипторы записи закрыты — «конец файла» */
            printf("[читатель] канал закрыт (конец файла)\n");
            fflush(stdout);
            break;
        }
    }

    printf("поток читатель закончил работу\n");
    fflush(stdout);
    pthread_exit((void *)2);
    return NULL;
}

/* ------------------------------------------------------------------ */
/* Основная программа                                                  */
/* ------------------------------------------------------------------ */
int main(int argc, char *argv[])
{
    printf("программа начала работу\n");

    /* --- Разбор аргументов командной строки через getopt() ---------- */

    /* getopt — разбор аргументов командной строки;
       argc, argv — стандартные параметры main();
       "m:"       — список допустимых ключей: 'm' требует аргумента (':');
       возвращает символ ключа или -1 когда аргументы закончились */
    int mode = 1; /* режим по умолчанию: pipe() с блокировкой */
    int opt;
    while ((opt = getopt(argc, argv, "m:")) != -1) {
        if (opt == 'm') {
            mode = atoi(optarg); /* optarg — строка-аргумент текущего ключа */
            if (mode < 1 || mode > 3) {
                fprintf(stderr, "Использование: %s -m <1|2|3>\n"
                        "  1 — pipe()  (блокирующий)\n"
                        "  2 — pipe2() (неблокирующий, Linux)\n"
                        "  3 — pipe() + fcntl() (неблокирующий, POSIX)\n",
                        argv[0]);
                return 1;
            }
        }
    }

    /* --- Регистрация обработчика SIGINT ------------------------------ */

    /* signal — замена системного обработчика сигнала SIGINT на sig_handler;
       SIGINT      — сигнал прерывания (Ctrl+C);
       sig_handler — наша функция-обработчик */
    signal(SIGINT, sig_handler);

    /* --- Создание неименованного канала в выбранном режиме ----------- */

    if (mode == 1) {
        /* pipe — создание неименованного канала в блокирующем режиме;
           pipefd[0] — дескриптор чтения, pipefd[1] — дескриптор записи;
           при чтении из пустого канала поток блокируется;
           при записи в полный канал поток блокируется */
        printf("режим 1: pipe() — блокирующий\n");
        if (pipe(pipefd) == -1) {
            perror("pipe");
            return 1;
        }

    } else if (mode == 2) {
        /* pipe2 — создание канала с флагом O_NONBLOCK (только Linux);
           O_NONBLOCK — оба дескриптора сразу переводятся в неблокирующий
           режим: read/write не блокируются, а возвращают -1 (errno=EAGAIN) */
        printf("режим 2: pipe2() — неблокирующий (Linux)\n");
        if (pipe2(pipefd, O_NONBLOCK) == -1) {
            perror("pipe2");
            return 1;
        }

    } else {
        /* Режим 3: pipe() + fcntl() — переносимый POSIX-способ */
        printf("режим 3: pipe() + fcntl() — неблокирующий (POSIX)\n");

        /* pipe — создаём канал в блокирующем режиме */
        if (pipe(pipefd) == -1) {
            perror("pipe");
            return 1;
        }

        /* fcntl — установка флага O_NONBLOCK на дескриптор чтения;
           F_SETFL   — команда: установить флаги состояния дескриптора;
           O_NONBLOCK — флаг неблокирующего режима */
        if (fcntl(pipefd[0], F_SETFL, O_NONBLOCK) == -1) {
            perror("fcntl pipefd[0]");
            return 1;
        }

        /* fcntl — установка флага O_NONBLOCK на дескриптор записи */
        if (fcntl(pipefd[1], F_SETFL, O_NONBLOCK) == -1) {
            perror("fcntl pipefd[1]");
            return 1;
        }
    }

    /* --- Создание потоков ------------------------------------------- */

    targs arg1 = {0}; /* параметры потока-писателя */
    targs arg2 = {0}; /* параметры потока-читателя */

    pthread_t id1, id2;

    /* pthread_create — создание потока-писателя */
    int rv1 = pthread_create(&id1, NULL, thread_writer, &arg1);
    if (rv1 != 0) {
        perror("pthread_create writer");
        return 1;
    }

    /* pthread_create — создание потока-читателя */
    int rv2 = pthread_create(&id2, NULL, thread_reader, &arg2);
    if (rv2 != 0) {
        perror("pthread_create reader");
        return 1;
    }

    printf("программа ждет нажатия клавиши\n");

    /* getchar — блокирующее ожидание нажатия <Enter> */
    getchar();

    printf("клавиша нажата\n");

    /* Устанавливаем флаги завершения обоих потоков */
    arg1.flag = 1;
    arg2.flag = 1;

    void *ec1 = NULL;
    void *ec2 = NULL;

    /* pthread_join — ожидание завершения потока-писателя */
    pthread_join(id1, &ec1);

    /* pthread_join — ожидание завершения потока-читателя */
    pthread_join(id2, &ec2);

    printf("поток писатель завершился с кодом: %ld\n", (long)ec1);
    printf("поток читатель завершился с кодом: %ld\n", (long)ec2);

    /* close — закрытие дескриптора записи канала */
    close(pipefd[1]);

    /* close — закрытие дескриптора чтения канала */
    close(pipefd[0]);

    printf("программа завершила работу\n");
    return 0;
}
