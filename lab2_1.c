/* Программа 1 — критический ресурс БЕЗ синхронизации.
   Два потока выводят символы на экран без каких-либо ограничений.
   Ожидаемый вывод: 121212121212... — потоки перемежаются произвольно. */

#include <stdio.h>
#include <pthread.h>
#include <unistd.h>

typedef struct {
    int  flag;  /* флаг завершения: 0 — работать, 1 — завершиться */
    char sym;   /* символ, который выводит поток */
} targs;

/* ------------------------------------------------------------------ */
/* Поточная функция 1                                                   */
/* ------------------------------------------------------------------ */
static void *thread_func1(void *arg)
{
    printf("поток 1 начал работу\n");
    fflush(stdout);

    targs *args = (targs *)arg; /* приводим void* к типу targs* */

    while (args->flag == 0) {  /* внешний цикл — работаем, пока флаг не установлен */

        /* === ВХОД В КРИТИЧЕСКИЙ УЧАСТОК (без синхронизации) === */
        for (int i = 0; i < 10; i++) {  /* 10 раз выводим символ — имитируем работу в КУ */
            putchar(args->sym);         /* вывод символа потока на экран (критический ресурс) */
            fflush(stdout);             /* сброс буфера — символ виден сразу */
            sleep(1);                   /* задержка для визуализации чередования потоков */
        }
        /* === ВЫХОД ИЗ КРИТИЧЕСКОГО УЧАСТКА === */

        sleep(1); /* задержка имитирует работу потока ВНЕ критического участка */
    }

    printf("\nпоток 1 закончил работу\n");
    fflush(stdout);

    /* pthread_exit — завершение текущего потока с кодом 1 */
    pthread_exit((void *)1);
    return NULL;
}

/* ------------------------------------------------------------------ */
/* Поточная функция 2                                                   */
/* ------------------------------------------------------------------ */
static void *thread_func2(void *arg)
{
    printf("поток 2 начал работу\n");
    fflush(stdout);

    targs *args = (targs *)arg; /* приводим void* к типу targs* */

    while (args->flag == 0) {  /* внешний цикл — работаем, пока флаг не установлен */

        /* === ВХОД В КРИТИЧЕСКИЙ УЧАСТОК (без синхронизации) === */
        for (int i = 0; i < 10; i++) {  /* 10 раз выводим символ — имитируем работу в КУ */
            putchar(args->sym);         /* вывод символа потока на экран (критический ресурс) */
            fflush(stdout);             /* сброс буфера */
            sleep(1);                   /* задержка для визуализации */
        }
        /* === ВЫХОД ИЗ КРИТИЧЕСКОГО УЧАСТКА === */

        sleep(1); /* задержка имитирует работу потока ВНЕ критического участка */
    }

    printf("\nпоток 2 закончил работу\n");
    fflush(stdout);

    /* pthread_exit — завершение текущего потока с кодом 2 */
    pthread_exit((void *)2);
    return NULL;
}

/* ------------------------------------------------------------------ */
/* Основная программа                                                   */
/* ------------------------------------------------------------------ */
int main(void)
{
    printf("программа начала работу\n");

    pthread_t id1, id2; /* идентификаторы потоков */

    targs arg1 = {0, '1'}; /* параметры потока 1: флаг=0, символ='1' */
    targs arg2 = {0, '2'}; /* параметры потока 2: флаг=0, символ='2' */

    /* pthread_create — создание потока 1;
       &id1         — указатель на дескриптор потока;
       NULL         — атрибуты по умолчанию;
       thread_func1 — поточная функция;
       &arg1        — передаваемые параметры */
    int rv1 = pthread_create(&id1, NULL, thread_func1, &arg1);
    if (rv1 != 0) { /* ненулевой код — ошибка создания потока */
        perror("pthread_create 1");
        return 1;
    }

    /* pthread_create — создание потока 2 (аналогично потоку 1) */
    int rv2 = pthread_create(&id2, NULL, thread_func2, &arg2);
    if (rv2 != 0) {
        perror("pthread_create 2");
        return 1;
    }

    printf("программа ждет нажатия клавиши\n");

    /* getchar — блокирующее ожидание нажатия <Enter>;
       потоки продолжают работать, пока ждём */
    getchar();

    printf("клавиша нажата\n");

    /* Устанавливаем флаги завершения — потоки завершатся
       после текущей итерации внешнего цикла */
    arg1.flag = 1;
    arg2.flag = 1;

    void *ec1 = NULL;
    void *ec2 = NULL;

    /* pthread_join — ожидание завершения потока 1;
       &ec1 — адрес для приёма кода завершения из pthread_exit */
    pthread_join(id1, &ec1);

    /* pthread_join — ожидание завершения потока 2 */
    pthread_join(id2, &ec2);

    printf("поток 1 завершился с кодом: %ld\n", (long)ec1);
    printf("поток 2 завершился с кодом: %ld\n", (long)ec2);

    printf("программа завершила работу\n");
    return 0;
}
