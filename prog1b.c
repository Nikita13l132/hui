/* Лабораторная работа 5 — Синхронизация процессов именованным семафором.
   Программа A: записывает символ '1' в общий файл и на экран.
   Программа B (prog1b.c): идентична, но записывает символ '2'.

   Обе программы запускаются в разных терминалах и обращаются к одному
   файлу, координируя доступ через общий именованный POSIX-семафор.

   Проверка нажатия клавиши <Enter> без блокировки выполняется
   функцией epoll_pwait() — вариант №8 по заданию. */

#define _GNU_SOURCE
#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include <unistd.h>
#include <fcntl.h>       /* open(), O_WRONLY, O_CREAT, O_APPEND */
#include <semaphore.h>   /* sem_open(), sem_wait(), sem_post(), sem_close(), sem_unlink() */
#include <sys/epoll.h>   /* epoll_create(), epoll_ctl(), epoll_pwait() */
#include <sys/stat.h>    /* S_IRUSR, S_IWUSR */
#include <signal.h>      /* sigset_t, sigemptyset() */

/* Имя именованного семафора — одинаковое в обеих программах.
   Стандарт требует, чтобы имя начиналось с символа '/' */
#define SEM_NAME  "/lab5_sem"

/* Имя общего файла — одинаковое в обеих программах */
#define FILE_NAME "lab5_out.txt"

/* Символ, который данная программа пишет в файл и на экран */
#define MY_CHAR   '2'

/* Число символов, выводимых за одно нахождение в критическом участке */
#define COUNT 10

int main(void)
{
    printf("программа B (символ '%c') начала работу\n", MY_CHAR);

    /* --- Создание экземпляра epoll для отслеживания STDIN ----------- */

    /* epoll_create — создание экземпляра epoll;
       параметр 1 игнорируется ядром (устаревший size), но должен быть > 0;
       возвращает дескриптор экземпляра epoll или -1 при ошибке */
    int epfd = epoll_create(1);
    if (epfd == -1) {
        perror("epoll_create");
        return 1;
    }

    /* Описываем событие, которое хотим отслеживать:
       EPOLLIN — есть данные для чтения (нажата клавиша <Enter>);
       data.fd — дескриптор, на котором ждём событие (STDIN_FILENO = 0) */
    struct epoll_event ev;
    memset(&ev, 0, sizeof(ev));
    ev.events  = EPOLLIN;
    ev.data.fd = STDIN_FILENO;

    /* epoll_ctl — регистрация дескриптора STDIN в экземпляре epoll;
       epfd          — дескриптор экземпляра epoll;
       EPOLL_CTL_ADD — команда: добавить дескриптор;
       STDIN_FILENO  — отслеживаемый дескриптор (стандартный ввод);
       &ev           — описание отслеживаемого события */
    if (epoll_ctl(epfd, EPOLL_CTL_ADD, STDIN_FILENO, &ev) == -1) {
        perror("epoll_ctl");
        return 1;
    }

    /* --- Создание (или открытие) именованного семафора -------------- */

    /* sem_open — создание или открытие именованного семафора;
       SEM_NAME     — имя семафора (начинается с '/');
       O_CREAT      — создать семафор если не существует;
       0600         — права доступа: чтение и запись для владельца;
       1            — начальное значение: 1 (семафор свободен),
                      что позволяет первому же процессу войти в КУ;
       возвращает указатель на семафор или SEM_FAILED при ошибке */
    sem_t *sem = sem_open(SEM_NAME, O_CREAT, 0600, 1);
    if (sem == SEM_FAILED) {
        perror("sem_open");
        return 1;
    }
    printf("программа B: семафор '%s' открыт\n", SEM_NAME);

    /* --- Создание (или открытие) общего файла ----------------------- */

    /* open — открытие файла для записи;
       O_WRONLY  — только запись;
       O_CREAT   — создать файл если не существует;
       O_APPEND  — каждая запись добавляется в конец файла;
       0644      — права: владелец читает/пишет, остальные читают */
    int fd = open(FILE_NAME, O_WRONLY | O_CREAT | O_APPEND, 0644);
    if (fd == -1) {
        perror("open");
        sem_close(sem);
        return 1;
    }
    printf("программа B: файл '%s' открыт\n", FILE_NAME);
    printf("программа B: для завершения нажмите <Enter>\n");

    /* Маска сигналов для epoll_pwait: пустая — сигналы не блокируем */
    sigset_t sigmask;
    /* sigemptyset — инициализация пустой маски сигналов */
    sigemptyset(&sigmask);

    struct epoll_event fired; /* структура для получения сработавшего события */

    /* --- Основной цикл --------------------------------------------- */
    while (1) {

        /* sem_wait — захват именованного семафора (блокирующий);
           уменьшает значение семафора на 1;
           если семафор == 0 (захвачен другим процессом) — блокируется
           и ждёт, пока sem_post не увеличит значение обратно до 1 */
        if (sem_wait(sem) == -1) {
            perror("sem_wait");
            break;
        }

        /* === ВХОД В КРИТИЧЕСКИЙ УЧАСТОК === */
        for (int i = 0; i < COUNT; i++) {
            char c = MY_CHAR;

            /* write — запись символа в общий файл;
               fd — дескриптор файла; &c — буфер; 1 — число байт */
            if (write(fd, &c, 1) == -1) {
                perror("write");
            }

            /* putchar — вывод символа на экран своего терминала */
            putchar(MY_CHAR);
            fflush(stdout);

            sleep(1); /* задержка 1 с для наглядности */
        }
        /* === ВЫХОД ИЗ КРИТИЧЕСКОГО УЧАСТКА === */

        /* sem_post — освобождение именованного семафора;
           увеличивает значение семафора на 1, разблокируя ожидающий
           процесс (программу B) */
        if (sem_post(sem) == -1) {
            perror("sem_post");
            break;
        }

        printf("\n"); /* перевод строки после блока символов */

        sleep(1); /* работа вне критического участка */

        /* Проверяем нажатие клавиши <Enter> без блокировки.
           epoll_pwait — ожидание события на отслеживаемых дескрипторах;
           epfd    — дескриптор экземпляра epoll;
           &fired  — буфер для получения сработавших событий;
           1       — максимальное число событий в буфере;
           0       — таймаут 0 мс: функция возвращается немедленно,
                     не блокируя программу (неблокирующий опрос);
           &sigmask — маска сигналов на время вызова (пустая);
           возвращает число событий (0 — клавиша не нажата,
                                     1 — нажата), -1 при ошибке */
        int rv = epoll_pwait(epfd, &fired, 1, 0, &sigmask);
        if (rv == -1) {
            perror("epoll_pwait");
            break;
        }
        if (rv == 1) {
            /* событие на STDIN: клавиша <Enter> нажата — выходим */
            printf("программа B: клавиша нажата, завершаем работу\n");
            break;
        }
        /* rv == 0: клавиша не нажата — продолжаем цикл */
    }

    /* --- Завершение работы ----------------------------------------- */

    /* close — закрытие дескриптора общего файла */
    close(fd);

    /* close — закрытие дескриптора экземпляра epoll */
    close(epfd);

    /* sem_close — закрытие именованного семафора в данном процессе;
       не удаляет семафор из системы */
    if (sem_close(sem) == -1) {
        perror("sem_close");
    }

    /* sem_unlink — удаление именованного семафора из файловой системы;
       после этого вызова семафор недоступен другим процессам;
       вызывать только из одной программы (той, что завершается последней) */
    if (sem_unlink(SEM_NAME) == -1) {
        perror("sem_unlink");
    }

    printf("программа B завершила работу\n");
    return 0;
}
